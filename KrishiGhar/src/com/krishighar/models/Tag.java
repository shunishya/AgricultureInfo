package com.krishighar.models;

public class Tag {
	private int cropId;
	private String tag;

	/**
	 * @return the cropId
	 */
	public int getCropId() {
		return cropId;
	}

	/**
	 * @param cropId
	 *            the cropId to set
	 */
	public void setCropId(int cropId) {
		this.cropId = cropId;
	}

	/**
	 * @return the tag
	 */
	public String getTag() {
		return tag;
	}

	/**
	 * @param tag
	 *            the tag to set
	 */
	public void setTag(String tag) {
		this.tag = tag;
	}

}
